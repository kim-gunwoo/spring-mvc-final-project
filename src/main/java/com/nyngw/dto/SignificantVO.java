package com.nyngw.dto;

public class SignificantVO {
	private String sf_number;	//특이사항번호
	private String sf_kind;		//특이사항종류
	private String sf_name;		//특이사항명
	public String getSf_number() {
		return sf_number;
	}
	public void setSf_number(String sf_number) {
		this.sf_number = sf_number;
	}
	public String getSf_kind() {
		return sf_kind;
	}
	public void setSf_kind(String sf_kind) {
		this.sf_kind = sf_kind;
	}
	public String getSf_name() {
		return sf_name;
	}
	public void setSf_name(String sf_name) {
		this.sf_name = sf_name;
	}
}
